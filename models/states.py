"""
==================================================
STATE MODEL

Shared state passed between all agents.

Version 6
Multi-Agent Architecture
==================================================
"""

from typing import TypedDict, List, Dict, Any


class LearningState(TypedDict, total=False):

    # -------------------------------------------------
    # INPUT
    # -------------------------------------------------

    student_question: str
    student_answer: str

    # -------------------------------------------------
    # SUPERVISOR
    # -------------------------------------------------

    current_agent: str

    # -------------------------------------------------
    # TEACHING AGENT
    # -------------------------------------------------

    topic: str
    explanation: str

    # -------------------------------------------------
    # QUIZ AGENT
    # -------------------------------------------------

    quiz: List[str]
    correct_answer: str

    # -------------------------------------------------
    # EVALUATION AGENT
    # -------------------------------------------------

    score: int
    feedback: str

    # -------------------------------------------------
    # MEMORY AGENT
    # -------------------------------------------------

    memory: object
    history: List[Dict[str, Any]]

    # -------------------------------------------------
    # REPORT AGENT
    # -------------------------------------------------

    teacher_report: Dict[str, Any]

    # -------------------------------------------------
    # SHARED OBJECTS
    # -------------------------------------------------

    llm: object

    # -------------------------------------------------
    # TOOLS
    # -------------------------------------------------

    tool_result: str

    # -------------------------------------------------
    # REACT
    # -------------------------------------------------

    reasoning: str

    # -------------------------------------------------
    # CHECKPOINT
    # -------------------------------------------------

    thread_id: str

    # -------------------------------------------------
    # HUMAN REVIEW
    # -------------------------------------------------

    approval: bool

    review_comment: str

    # -------------------------------------------------
    # RAG
    # -------------------------------------------------

    documents: List[str]

    context: str

    retrieved_chunks: List[str]

    vector_store: object

    retriever: object
